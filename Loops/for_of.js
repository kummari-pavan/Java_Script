// for...of Loop
// The for...of loop is used to iterate over iterable objects like arrays, strings, or sets. It iterates over the values of the iterable.


// for (variable of iterable) {
//   // code to be executed
// }

let arr = [10, 20, 30, 40];
for (let value of arr) {
  console.log(value);  // Outputs: 10, 20, 30, 40
}

let l=["3","6","7","9","2"];
for (let i of l){
    console.log(i)
}